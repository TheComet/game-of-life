#include <Cell.hpp>

// ----------------------------------------------------------------------------
Cell::Cell() :
    m_IsAlive(false)
{
}

// ----------------------------------------------------------------------------
Cell::~Cell()
{
}

// ----------------------------------------------------------------------------
void Cell::kill()
{
    m_IsAlive = false;
}

// ----------------------------------------------------------------------------
void Cell::revive()
{
    m_IsAlive = true;
}

// ----------------------------------------------------------------------------
bool Cell::isAlive()
{
    return m_IsAlive;
}
